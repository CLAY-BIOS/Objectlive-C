    如何设计一个类：
        类的作用：用来描述一群具有相同特征和行为的事物。
        设计类的三要素。
        -> 类的名字，你要描述的这类事物叫什么名字。
        -> 这类事物具有的相同的特征，这类事物拥有什么。
        -> 这类事物具有的共同行为，这类事物会做什么。
    如何找到类：
        名词提炼法：分析整个业务流程，分析出现类那些名词，这些名词就是你要找的类。
        三辆坦克发射了六枚炮弹打中了三架飞机。
            坦克类：
                特征：型号·大小·颜色·重量·材质·射程
                行为：行驶·发射
            炮弹类：
                特征：型号·大小·威力·颜色·重量
                行为：飞·爆炸
            飞机类：
                特征：型号·大小·数量
                行为：飞·爆炸
 ------------------------------------------------------------------------------------------
    现有对象还是先有类？
        从现实角度：一定是先有对象再有类。
        从代码角度：一定是先有类再有对象。
    
    再一次确定类的作用：
            描述一群具有相同特征和行为的事物。
 
    如何定义类：
        1.类的三要素。
        2.定义类的语法。
            1.位置·直接写在原文件之中，不要写在main函数之中。
            2.类的定义分为两个部分。
                ->类的声明
                    @interface 类名：NSObject
                    {
                        这类事物具有的共同的特征，将他们定义为变量。
                    }
                    功能就是一个方法，将方法的声明写在这里。
                    @end
                ->类的实现
                    @implementation 类名
                    将方法的实现写在这里。
                    @end
        3.几点注意：
            1.类必须要有声明和实现。
            2.类明用你描述事物的名称来命名就行了，类名的每一个单词的首字母必须要大写开头。
            3.NSObject 表示继承NSObject类的事物
            4.用来表示这类事物的共同特征的变量必须要定义在@interface的大括号之中。
            5.定义在大括号中用来表示这类事物的共同特征的变量我们叫做 属性 成员变量 实例变量 字段
            6.为类定义属性的时候，属性的名词必须要以_开头。
                语法是固定的。
                 @interface 类名 : NSObject
                 {
                     这类事物具有的共同特征定义为变量
                     数据类型 变量名1；
                     数据类型 变量名2:
                     ..............
                 }
                 @end
 
                 @implementation 类名

                 @end
                
