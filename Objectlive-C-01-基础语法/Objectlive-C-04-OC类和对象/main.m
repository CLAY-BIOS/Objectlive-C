        类是模版·类的对象是根据这个模版创建出来的。
        类的模版中有什么，对象中就有什么，绝对不能多，也绝对不能少。
        类是制造月饼的模具，模具不能直接吃，所以类不能直接使用。
        对象是根据模具制造出来的月饼，模具上有什么月饼上就有什么，不会多也不会少。
        月饼可以吃，所以月饼是对象。
